Marks = int(input("Enter your marks: "))

if(Marks >= 90):
    print("A+ Grade")

elif(Marks >= 80):
    print("A Grade")

elif(Marks >= 70):
    print("B Grade")

elif(Marks >= 60):
    print("C Grade")

else:
    print("Fail")


    



